age = int(input("Enter your age: "))

if (age >= 18):
    print("You're an adult now and you can vote, drive, and drink responsibly!")

elif (age >= 18 and age <= 25):
    print("You're 17!")

elif (age == 17):
    print("You're 17!")

else:
    print("I don't really want to know your age!")

